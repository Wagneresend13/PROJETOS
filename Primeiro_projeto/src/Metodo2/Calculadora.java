package Metodo2;

public class Calculadora {
	
	double num1,num2;
	
	
	
	public double operacaoSoma (double num1,double num2) {
		
		double soma =  num1 + num2;
		return soma;
	}
	
	public double operacaoSubtracao (double num1,double num2) {
		
		double subtracao =  num1 - num2;
		
		return subtracao;
	}
	
	public double operacaoMultiplicacao (double num1,double num2) {
		
		double multiplicacao =  num1 * num2;
		
		return multiplicacao;
	}
	
	public double operacaoDivisao (double num1,double num2) {
		
		double divisao =  num1 / num2;
		
		return divisao;
	}
	
	
	
	
	
	

}
